/*
- Operador + adiciona os 2 números à esquerda e à direita

- Operador * multiplica os 2 números à esquerda e à direita
*/

let x = 2; // Cria a variável x com valor 2
let y = 3; // Cria a variável y com valor 3

x + y; // Adiciona x e y. Nesse caso, a soma dá 5.

x * y; // Multiplicação x e y. Nesse caso, o resultado é 6.

// ERRADO:
// 2 x 5;  // X não é um operador de multiplicação

// CERTO:
// 2 * 5;
