// Comparação com número
console.log("\nComparação com número");
console.log(15 === 20);
console.log(20 === 20);
console.log(5 === 5);

// Comparação com variável
console.log("\nComparação com variável");
let numero = 20;
console.log(numero === 20);
console.log(numero === 10);

// Comparação com dois e três sinais de igualdade
console.log("\nComparação com dois e três sinais de igualdade");
console.log(15 == 20); // Errado
console.log(15 === 20); // Certo

// Comparação com diferença
console.log("\nComparação com diferença");
console.log(15 !== 10);
console.log(5 !== 5);
