// --- Operador Maior (>) ---
console.log("--- Operador Maior (>) ---");
console.log(10 > 5);
console.log(5 > 8);
console.log(10 > 10);

// usado com variáveis
let a = 20;
let b = 10;
let resultado = a > b;
console.log(resultado + " - 'a' é estritamente maior que 'b'");

// variáveis e números
let limite = 20;
limite > 30;
console.log(limite > 30);
console.log("\n");

// --- Operador Menor (<) ---
console.log("--- Operador Menor (<) ---");
console.log(5 < 10);
console.log(10 < 10);
console.log(5 < 3);
console.log("\n");

// --- Operador Maior ou igual (>=) ---
console.log("--- Operador Maior ou igual (>=) ---");
console.log(20 >= 10);
console.log(10 >= 10);
console.log(5 >= 15);
console.log("\n");

// --- Operador Menor ou igual (<=) ---
console.log("--- Operador Maior  ou igual (<=) ---");
console.log(10 <= 20);
console.log(10 <= 10);
console.log(15 <= 5);
